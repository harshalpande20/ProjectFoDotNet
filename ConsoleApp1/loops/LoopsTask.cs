using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp1.loops
{
    public class LoopsTask
    {
        public static void Task()
        {
            // Print numbers from 1 to 10 using a for loop
            for (int i = 1; i <= 10; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}
